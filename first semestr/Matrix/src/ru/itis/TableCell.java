package ru.itis;

public class TableCell {

    int i;
    int j;
    int value;

    public TableCell(int i, int j, int value) {
        this.i = i;
        this.j = j;
        this.value = value;
    }
    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public int getValue() {
        return value;
    }
}
